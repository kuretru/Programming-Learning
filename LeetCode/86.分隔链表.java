/**
 * Definition for singly-linked list.
 * public class ListNode {
 *     int val;
 *     ListNode next;
 *     ListNode(int x) { val = x; }
 * }
 */
class Solution {
    public ListNode partition(ListNode head, int x) {
        if (head == null) {
            return null;
        }
        ListNode smallRoot = new ListNode(0);
        ListNode small = smallRoot;
        ListNode largeRoot = new ListNode(0);
        ListNode large = largeRoot;
        while (head != null) {
            if (head.val < x) {
                small.next = head;
                small = small.next;
            } else {
                large.next = head;
                large = large.next;
            }
            head = head.next;
        }
        small.next = largeRoot.next;
        large.next = null;
        return smallRoot.next;
    }
}
